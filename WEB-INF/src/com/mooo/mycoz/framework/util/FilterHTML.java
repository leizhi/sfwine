package com.mooo.mycoz.framework.util;

public class FilterHTML {

	/**
	 * This method takes a string which may contain HTML tags (ie, <b>, <table>,
	 * etc) and converts the '<' and '>' characters to their HTML escape
	 * sequences.
	 * 
	 * @param input
	 *            The text to be converted.
	 * @return The input string with the characters '<' and '>' replaced with
	 *         &lt; and &gt; respectively.
	 */
	public synchronized static String applyFilters(String input) {
		
		// Check if the string is null or zero length -- if so, return
		// what was sent in.
		if (input == null || input.length() == 0) {
			return input;
		}
		
		// Use a StringBuffer in lieu of String concatenation -- it is
		// much more efficient this way.
		StringBuffer buf = new StringBuffer(input.length() + 6);
		char ch = ' ';
		for (int i = 0; i < input.length(); i++) {
			ch = input.charAt(i);
			if (ch == '<') {
				buf.append("&lt;");
			} else if (ch == '>') {
				buf.append("&gt;");
			} else {
				buf.append(ch);
			}
		}
		return buf.toString();
	}
}
